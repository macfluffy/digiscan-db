export const seedCardTraitsData = async () => {
    try {
        await sequelize.authenticate();
        console.log("Database connection established.");

        await sequelize.sync({ force: true });
        console.log("Forcing synchronization.");

        await CardTraits.bulkCreate(seedData);
        console.log("Card Traits table seeded.");
    } 
    catch (error) {
        console.error("Could not initialise Card Traits Table: ", error);
    }
    finally {
        await sequelize.close();
    }
};

seedCardTraitsData();